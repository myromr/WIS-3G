<?php
// Arithmetic operators
$a = 10;
$b = 5;

$addition = $a + $b;
$subtraction = $a - $b;
$multiplication = $a * $b;
$division = $a / $b;

echo "Addition: $addition<br>";
echo "Subtraction: $subtraction<br>";
echo "Multiplication: $multiplication<br>";
echo "Division: $division<br>";

// Assignment operators
$x = 5;
$y = 3;

$x += $y; // Adds $y to $x
$y -= $x; // Subtracts $x from $y

echo "x after addition: $x<br>";
echo "y after subtraction: $y";
?>